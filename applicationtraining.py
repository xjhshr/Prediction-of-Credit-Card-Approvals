import pandas as pd
import numpy as np
names=['ID_vector_Appl_train','Labels_vector_train','Gender_vector_train',\
       'Car_vector_train','Property_vector_train','Children_vector_train',\
       'IncomeAnnual_vector_train','IncomeType_vector_train','Edu_vector_train',\
       'Housing_vector_train','Age_vector_train','WorkingAge_vector_train']
df = pd.read_csv('drive/My Drive/CreditCardTraining_set.csv',header=None,sep=',',names=names)
print(df)

labels=np.array(df['Labels_vector_train'])
data=np.array(df[['Gender_vector_train','Car_vector_train','Property_vector_train','Children_vector_train',\
       'IncomeAnnual_vector_train','IncomeType_vector_train','Edu_vector_train',\
       'Housing_vector_train','Age_vector_train','WorkingAge_vector_train']])

from sklearn.linear_model import LogisticRegression
from sklearn.model_selection import train_test_split

x_train, x_test, y_train, y_test = train_test_split(data, labels, test_size=0.2, random_state=0)
logisticReg = LogisticRegression()
logisticReg.fit(x_train, y_train)

logisticReg.predict(x_test[0:30])

predictions = logisticReg.predict(x_test)
score = logisticReg.score(x_test, y_test)
print(score)

